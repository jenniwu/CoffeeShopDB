package objects;

public class SupplierInfo {

    String sphone;
    String sname;

    public SupplierInfo(String sphone, String sname) {
        this.sphone = sphone;
        this.sname = sname;
    }

    public String getSphone() {
        return sphone;
    }

    public String getSname() {
        return sname;
    }
}
